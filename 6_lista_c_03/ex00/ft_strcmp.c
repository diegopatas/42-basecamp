int	ft_strcmp(char *s1, char *s2)
{
	int	value;
	int	i;

	value = 0;
	i = 0;
	while ((s1[i] != '\0') || (s2[i] != '\0'))
	{
		value = s1[i] - s2[i];
		if (value != 0)
			return (value);
		i++;
	}
	return (value);
}
